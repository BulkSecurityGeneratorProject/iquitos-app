    @Test
    @Transactional
    public void updateProduct() throws Exception {
        // Initialize the database
        productRepository.saveAndFlush(product);

        int databaseSizeBeforeUpdate = productRepository.findAll().size();

        // Update the product
        Product updatedProduct = productRepository.findById(product.getId()).get();
        // Disconnect from session so that the updates on updatedProduct are not directly saved in db
        em.detach(updatedProduct);
        updatedProduct
            .code(UPDATED_CODE)
            .description(UPDATED_DESCRIPTION)
            .dni(UPDATED_DNI)
            .expirationDate(UPDATED_EXPIRATION_DATE)
            .isFavorite(UPDATED_IS_FAVORITE)
            .visibleToSell(UPDATED_VISIBLE_TO_SELL)
            .image(UPDATED_IMAGE)
            .imageContentType(UPDATED_IMAGE_CONTENT_TYPE)
            .stock(UPDATED_STOCK)
            .stockLimitNotification(UPDATED_STOCK_LIMIT_NOTIFICATION)
            .productType(UPDATED_PRODUCT_TYPE);
        ProductDTO productDTO = productMapper.toDto(updatedProduct);

        restProductMockMvc.perform(put("/api/products")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(productDTO)))
            .andExpect(status().isOk());

        // Validate the Product in the database
        List<Product> productList = productRepository.findAll();
        assertThat(productList).hasSize(databaseSizeBeforeUpdate);
        Product testProduct = productList.get(productList.size() - 1);
        assertThat(testProduct.getCode()).isEqualTo(UPDATED_CODE);
        assertThat(testProduct.getDescription()).isEqualTo(UPDATED_DESCRIPTION);
        assertThat(testProduct.getDni()).isEqualTo(UPDATED_DNI);
        assertThat(testProduct.getExpirationDate()).isEqualTo(UPDATED_EXPIRATION_DATE);
        assertThat(testProduct.isIsFavorite()).isEqualTo(UPDATED_IS_FAVORITE);
        assertThat(testProduct.isVisibleToSell()).isEqualTo(UPDATED_VISIBLE_TO_SELL);
        assertThat(testProduct.getImage()).isEqualTo(UPDATED_IMAGE);
        assertThat(testProduct.getImageContentType()).isEqualTo(UPDATED_IMAGE_CONTENT_TYPE);
        assertThat(testProduct.getStock()).isEqualTo(UPDATED_STOCK);
        assertThat(testProduct.getStockLimitNotification()).isEqualTo(UPDATED_STOCK_LIMIT_NOTIFICATION);
        assertThat(testProduct.getProductType()).isEqualTo(UPDATED_PRODUCT_TYPE);

        // Validate the Product in Elasticsearch
        verify(mockProductSearchRepository, times(1)).save(testProduct);
    }

