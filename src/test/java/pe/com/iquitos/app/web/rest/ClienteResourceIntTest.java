    @Test
    @Transactional
    public void updateCliente() throws Exception {
        // Initialize the database
        clienteRepository.saveAndFlush(cliente);

        int databaseSizeBeforeUpdate = clienteRepository.findAll().size();

        // Update the cliente
        Cliente updatedCliente = clienteRepository.findById(cliente.getId()).get();
        // Disconnect from session so that the updates on updatedCliente are not directly saved in db
        em.detach(updatedCliente);
        updatedCliente
            .nombre(UPDATED_NOMBRE)
            .codigo(UPDATED_CODIGO)
            .direccion(UPDATED_DIRECCION)
            .correo(UPDATED_CORREO)
            .telefono(UPDATED_TELEFONO)
            .fechaDeNacimiento(UPDATED_FECHA_DE_NACIMIENTO)
            .sexo(UPDATED_SEXO)
            .estatusCivil(UPDATED_ESTATUS_CIVIL)
            .imagen(UPDATED_IMAGEN)
            .imagenContentType(UPDATED_IMAGEN_CONTENT_TYPE)
            .tipoDeCliente(UPDATED_TIPO_DE_CLIENTE);
        ClienteDTO clienteDTO = clienteMapper.toDto(updatedCliente);

        restClienteMockMvc.perform(put("/api/clientes")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(clienteDTO)))
            .andExpect(status().isOk());

        // Validate the Cliente in the database
        List<Cliente> clienteList = clienteRepository.findAll();
        assertThat(clienteList).hasSize(databaseSizeBeforeUpdate);
        Cliente testCliente = clienteList.get(clienteList.size() - 1);
        assertThat(testCliente.getNombre()).isEqualTo(UPDATED_NOMBRE);
        assertThat(testCliente.getCodigo()).isEqualTo(UPDATED_CODIGO);
        assertThat(testCliente.getDireccion()).isEqualTo(UPDATED_DIRECCION);
        assertThat(testCliente.getCorreo()).isEqualTo(UPDATED_CORREO);
        assertThat(testCliente.getTelefono()).isEqualTo(UPDATED_TELEFONO);
        assertThat(testCliente.getFechaDeNacimiento()).isEqualTo(UPDATED_FECHA_DE_NACIMIENTO);
        assertThat(testCliente.getSexo()).isEqualTo(UPDATED_SEXO);
        assertThat(testCliente.getEstatusCivil()).isEqualTo(UPDATED_ESTATUS_CIVIL);
        assertThat(testCliente.getImagen()).isEqualTo(UPDATED_IMAGEN);
        assertThat(testCliente.getImagenContentType()).isEqualTo(UPDATED_IMAGEN_CONTENT_TYPE);
        assertThat(testCliente.getTipoDeCliente()).isEqualTo(UPDATED_TIPO_DE_CLIENTE);

        // Validate the Cliente in Elasticsearch
        verify(mockClienteSearchRepository, times(1)).save(testCliente);
    }

